package Service;

public class car {
    String car_number;
    String car_brand;
    String car_intime;
    String car_info;
    String car_position;
    String owner;

    public String getCar_number() {
        return car_number;
    }

    public void setCar_number(String car_number) {
        this.car_number = car_number;
    }

    public String getCar_brand() {
        return car_brand;
    }

    public void setCar_brand(String car_brand) {
        this.car_brand = car_brand;
    }

    public String getCar_intime() {
        return car_intime;
    }

    public void setCar_intime(String car_intime) {
        this.car_intime = car_intime;
    }

    public String getCar_info() {
        return car_info;
    }

    public void setCar_info(String car_info) {
        this.car_info = car_info;
    }

    public String getCar_position() {
        return car_position;
    }

    public void setCar_position(String car_position) {
        this.car_position = car_position;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }
}
